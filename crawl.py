def trans_time(time_str):
    # "20201014093500000"
    l = time_str.split(" ")
    s = l[1].split(":")
    d = l[0].replace('/', '').replace("-", '')
    h = s[0] if len(s[0]) == 2 else '0' + s[0]
    m = s[1]
    s = '00000'
    new_time = d + h + m + s
    return new_time


